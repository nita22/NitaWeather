public class WeatherItem {

    private String mWeatherText;
    private String mDate;
    private String mLow;
    private String mHigh;

    public String getmWeatherText() {
        return mWeatherText;
    }

    public void setmWeatherText(String mWeatherText) {
        this.mWeatherText = mWeatherText;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmLow() {
        return mLow;
    }

    public void setmLow(String mLow) {
        this.mLow = mLow;
    }

    public String getmHigh() {
        return mHigh;
    }

    public void setmHigh(String mHigh) {
        this.mHigh = mHigh;
    }
}
